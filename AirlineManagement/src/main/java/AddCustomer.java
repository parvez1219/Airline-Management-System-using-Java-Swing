    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String firstname=fname.getText();
        String lastname=lname.getText();
        String passport=pid.getText();
        String nic=nicid.getText();
        String address=addr.getText();
        String gender="";
        if(g1.isSelected())
            gender="Male";
        else
            gender="Female";
        
        String contact=cno.getText();
        String custid=jCustomerId.getText();
        
        DateFormat df=new SimpleDateFormat("yyyy-MM-dd");
        String date=df.format(dob.getDate());
        
        try {                                         
            // TODO add your handling code here:
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
            con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost/airlinedb","root","root");
            pre=con.prepareStatement("Insert into customers(CustomerId,FirstName,LastName,Passport,NIC,Address,DOB,Gender,Contact) Values(?,?,?,?,?,?,?,?,?)");
            pre.setString(1,custid);
            pre.setString(2,firstname);
            pre.setString(3,lastname);
            pre.setString(4,passport);
            pre.setString(5,nic);
            pre.setString(6,address);
            pre.setString(7,date);
            pre.setString(8,gender);
            pre.setString(9,contact);
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"Customer Added");
        } catch (SQLException ex) {
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed


