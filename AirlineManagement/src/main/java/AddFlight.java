    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String flighid=flightId.getText();
        String flightname=name.getText();
        String arr=arrival.getText();
        String dep=departure.getText();
        String noseat=nos.getText();
        String cost=fare.getText();
        DateFormat df=new SimpleDateFormat("yyyy-MM-dd");
        String d=df.format(date.getDate());
        String tt=time.getText();
        
        
        
        
        try {                                         
            // TODO add your handling code here:
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
            }
            con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost/airlinedb","root","root");
            pre=con.prepareStatement("Insert into flights(flightid,flightName,arrival,departure,seats,fare,date,timetaken) Values(?,?,?,?,?,?,?,?)");
            pre.setString(1,flighid);
            pre.setString(2,flightname);
            pre.setString(3,arr);
            pre.setString(4,dep);
            pre.setString(5,noseat);
            pre.setString(6,cost);
            pre.setString(7,d);
            pre.setString(8,tt);
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"Flight Added");
        } catch (SQLException ex) {
                Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed


